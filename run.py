import time

def machineprint(text, delay = 0.1):
    for letter in text:
        print(letter, end="", flush=True)
        time.sleep(delay)
    print()


def welcome_message():
    machineprint("""
        Hello there! 
        Welcome to this little game of Hangman!



    """)



